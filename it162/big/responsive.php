<section>
    <h2 class="subheader">Responsive vs Native Apps</h2>
    <p>
        The differences between responsive websites and mobile apps.
        The web world was built for desktop users,
        but day by day, the traffic of the web is moving to mobile devices.
        In 2020, more than “63% of all US
        online traffic comes from smartphones and tablets(Leftronic, 2020, para. 4 ),
        ” when in 2010 the internet usage was lower than 4%, according to thenextweb.com
        website.
        That is why it is essential to create content for mobile devices.
        The question is: Responsive web design or mobile apps, which is better?
    </p>
    <p>In the first place, responsive web design is a web page’s capacity to render
        all devices and screen sizes. Commonly, web developers build webpages for
        desktop view. Then they modify the webpage with CSS media queries, so when
        the webpage is open on a mobile device, the webpage can be displayed
        according to the screen size.
    </p>
    <p>
        Pros of responsive design:
    </p>
    <ul>
        <li>Faster development</li>
        <li>Works on any platform and device</li>
        <li>Easier to update</li>
        <li>Can be found easily</li>
    </ul>
    <p>Cons of responsive design: </p>
    <ul>
        <li>Slower than mobile apps</li>
        <li>Fewer functionalities </li>
        <li>Don’t work without internet</li>
    </ul>

        <p>On the other hand, a mobile app is an application software designed to run on a mobile device. These apps are
            developed for different operating systems like Android, iOS, or Windows. These apps have to be downloaded
            from stores and installed on the devices to work.
            Pros of mobile apps:
        </p>
        <ul>
            <li>Personalize experience</li>
            <li>Faster processing</li>
            <li>Work offline</li>
            <li>More functionalities
            </li>
            <li>More security
            </li>
        </ul>
        <p>Cons of mobile devices: </p>
        <ul>
            <li>Longer time to develop</li>
            <li>Can’t be found easily</li>
            <li>Has to be downloaded</li>
            <li>Regular updating by user</li>
        </ul>
        <p>In conclusion, the usage of mobile devices is increasing rapidly, 
            and developers have to keep that in mind when creating content.
             Both responsive design and mobile apps have their pros and cons,
              and each organization has to choose which one works better for 
              them according to the public they are trying to reach. If the
               money is not a problem, the best option is to have both and 
               take advantage of both responsive and apps’ benefits.</p>






</section>
